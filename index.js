function add(a,b) {
  return a + b
}
function subtract(a,b) {
  return a - b
}
function multiply(a,b) {
  return a * b
}
function divide (a, b) {
  return a / b
}

function inc(n) {
  var result = n
  result += 1
  return result
}

function dec(n) {
  return n - 1
}

